// Задача 3. Дан массив arr. Найдите среднее арифметическое его элементов. 
function average(arr) {
    if (arr.length === 0) return 0;      // Проверяем, не пустой ли массив
    let sum = arr.reduce((acc, num) => acc + num, 0);    // Суммируем элементы
    return sum / arr.length;     // Находим среднее арифметическое 
}

const arr = [1, 2, 3, 4, 5, 6];
console.log(average(arr)); // Вывод: 3

