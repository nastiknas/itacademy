// Задача 1. Дана строка из четного количества цифр. 
// Проверьте, что сумма первой половины цифр равняется сумме второй половине цифр. 
// Если это так - выведите 'да', в противном случае выведите 'нет'.

const str = "123456";      // Создаем строку из четного количества цифр
const mid = str.length / 2;

const firstHalf = str.slice(0, mid).split('').map(Number); //
const secondHalf = str.slice(mid).split('').map(Number);

const sumFirstHalf = firstHalf.reduce((a, b) => a + b, 0);    // Суммируем элементы первой части
const sumSecondHalf = secondHalf.reduce((a, b) => a + b, 0);   // Суммируем элементы второй части

if (sumFirstHalf === sumSecondHalf) {
  console.log('Да');
} else {
  console.log('Нет');
}


